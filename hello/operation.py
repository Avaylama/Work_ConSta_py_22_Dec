num1 = int(input("Enter the number"))
num2 = int(input("Enter the number"))
ope = input("Enter the operator sign")
if ope == "+":
    print(num1 + num2) 
elif ope == "-":
     print(num1 - num2)  
elif ope == "*":
     print(num1 * num2) 
elif ope == "**":
     print(num1 ** num2) 
else:
     print("invalid operator sign")
    
